import Toast, { ToastConfig } from "react-native-toast-message";
import { ToastMessageItem } from "./ToastMessageItem";

type Props = {
  [key: string]: any;
};

const toastConfig: ToastConfig = {
  successToast: ({
    text1,
    text2,
    props,
  }: {
    text1?: string;
    text2?: string;
    props: Props;
  }) => (
    <ToastMessageItem
      type="success"
      text1={text1 || ""}
      text2={text2 || ""}
      icon={props.icon}
      onClose={props.onClose}
    />
  ),
  errorToast: ({
    text1,
    text2,
    props,
  }: {
    text1?: string;
    text2?: string;
    props: Props;
  }) => (
    <ToastMessageItem type="error" text1={text1 || ""} text2={text2 || ""} />
  ),
  infoToast: ({
    text1,
    text2,
    props,
  }: {
    text1?: string;
    text2?: string;
    props: Props;
  }) => (
    <ToastMessageItem type="info" text1={text1 || ""} text2={text2 || ""} />
  ),
  warningToast: ({
    text1,
    text2,
    props,
  }: {
    text1?: string;
    text2?: string;
    props: Props;
  }) => (
    <ToastMessageItem type="warning" text1={text1 || ""} text2={text2 || ""} />
  ),
};

export const ToastMessage = () => {
  return <Toast config={toastConfig} position="top" />;
};
